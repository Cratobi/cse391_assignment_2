const quoteChanger = color => {
  const quoteBox = document.querySelector('#quote-box')
  const quote = document.querySelector('#quote')
  const quoteAuthor = document.querySelector('#quote-author')

  quoteBox.classList.remove(quoteBox.classList[1])
  quoteBox.classList.remove(quoteBox.classList[1])
  quoteBox.classList.add(color)
  
  const rand = Math.floor(Math.random() * QUOTE.length/2) * 2
  quote.textContent = QUOTE[rand]
  quoteAuthor.textContent = QUOTE[rand+1]
}

const heroConverter = () => {
  const heroConverterInput = document.querySelector('#converter-input')
  const heroConverterOption = document.querySelector('#converter-option')
  const heroConverterResult = document.querySelector('#converter-result')

  const value = heroConverterInput.value
  const option = heroConverterOption.value

  if (option === 'lb_kg') {
    heroConverterResult.textContent = Math.round(value * 0.45359237 * 100) / 100
  } else if (option === 'kg_lb') {
    heroConverterResult.textContent = Math.round(value / 0.45359237 * 100) / 100
  }
}

const seriesProcessor = () => {
  const seriesInput = document.querySelector('#series-input')

  const seriesMax = document.querySelector('#series-max')
  const seriesMin = document.querySelector('#series-min')
  const seriesSum = document.querySelector('#series-sum')
  const seriesAvg = document.querySelector('#series-avg')
  const seriesRev = document.querySelector('#series-rev')

  const seriesValues = seriesInput.value.split(/[\r\n\s,]+/)
  const seriesNumbers = seriesValues.filter(n => n).map(n => Number(n))
  const seriesReverse = seriesNumbers.reverse()
  const seriesLength = seriesNumbers.length

  let sum = 0

  seriesNumbers.map(n => (sum += n))
  console.log(seriesNumbers)

  seriesMax.textContent = Math.max(...seriesNumbers)
  seriesMin.textContent = Math.min(...seriesNumbers)
  seriesSum.textContent = sum
  seriesAvg.textContent = sum / seriesLength
  seriesRev.textContent = seriesReverse.join(',')
}

const magicClear = () => {
  const magicBox = document.querySelector('#magic-box')

  magicBox.value = ''
}
const magicCapitalize = () => {
  const magicBox = document.querySelector('#magic-box')
  const caps = magicBox.dataset.caps

  if (caps === 'lower') {
    magicBox.value = magicBox.value.toLowerCase()
    magicBox.dataset.caps = 'upper'
  } else {
    magicBox.value = magicBox.value.toUpperCase()
    magicBox.dataset.caps = 'lower'
  }
}
const magicStrip = () => {
  const magicBox = document.querySelector('#magic-box')
  let lines = magicBox.value.split(/\r?\n/)
  lines = lines.filter(n => n).map(n => n.trim())

  magicBox.value = lines.join('\n')
}
const magicSort = () => {
  const magicBox = document.querySelector('#magic-box')
  let lines = magicBox.value.split(/\r?\n/)
  lines = lines.sort()

  magicBox.value = lines.join('\n')
}
const magicReverse = () => {
  const magicBox = document.querySelector('#magic-box')
  let lines = magicBox.value.split(/\r?\n/)
  lines = lines.reverse()

  magicBox.value = lines.join('\n')
}
const magicAdd = () => {
  const magicBox = document.querySelector('#magic-box')
  let lines = magicBox.value.split(/\r?\n/)

  lines = lines.map((n, index) => `${index + 1}. ${n}`)

  magicBox.value = lines.join('\n')
}
const magicShuffle = () => {
  const magicBox = document.querySelector('#magic-box')
  const lines = magicBox.value.split(/\r?\n/)

  magicBox.value = lines
    .map(value => ({ value, sort: Math.random() }))
    .sort((a, b) => a.sort - b.sort)
    .map(({ value }) => value)
    .join('\n')
}
